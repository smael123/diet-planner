package appgui.beans;

/**
 *
 * @author lucy
 */
public class Meat {
    private int id;
    private String foodType;
    private String meatType;
    private int egg;
    private int seafood;
    private int redMeat;
    private int shellfish;
    private int shrimp;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the foodType
     */
    public String getFoodType() {
        return foodType;
    }

    /**
     * @param foodType the foodType to set
     */
    public void setFoodType(String foodType) {
        this.foodType = foodType;
    }

    /**
     * @return the meatType
     */
    public String getMeatType() {
        return meatType;
    }

    /**
     * @param meatType the meatType to set
     */
    public void setMeatType(String meatType) {
        this.meatType = meatType;
    }

    /**
     * @return the egg
     */
    public int getEgg() {
        return egg;
    }

    /**
     * @param egg the egg to set
     */
    public void setEgg(int egg) {
        this.egg = egg;
    }

    /**
     * @return the seafood
     */
    public int getSeafood() {
        return seafood;
    }

    /**
     * @param seafood the seafood to set
     */
    public void setSeafood(int seafood) {
        this.seafood = seafood;
    }

    /**
     * @return the redMeat
     */
    public int getRedMeat() {
        return redMeat;
    }

    /**
     * @param redMeat the redMeat to set
     */
    public void setRedMeat(int redMeat) {
        this.redMeat = redMeat;
    }

    /**
     * @return the shellfish
     */
    public int getShellfish() {
        return shellfish;
    }

    /**
     * @param shellfish the shellfish to set
     */
    public void setShellfish(int shellfish) {
        this.shellfish = shellfish;
    }

    /**
     * @return the shrimp
     */
    public int getShrimp() {
        return shrimp;
    }

    /**
     * @param shrimp the shrimp to set
     */
    public void setShrimp(int shrimp) {
        this.shrimp = shrimp;
    }
}
